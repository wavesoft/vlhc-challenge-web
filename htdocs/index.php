									</div>
									<div id="accordion75" class="panel-collapse collapse">
										<div class="panel-body">
											<strong>A:</strong> If the VM will not stop, even after doing the steps mentioned in Q2, it's probably because it is in a state called Guru Mediation. This means that VirtualBox encountered a critical problem and it paused the Virtual Machine in order for a "Guru" to have a look. The only way to get over this error is to kill the VBoxHeadless process and refresh the challenge website:                      
											<ul style="margin-left:15px">
												<li>
													<strong>Windows</strong>
													<ul style="margin-left:15px">
														<li>Start the Windows Task Manager</li>
														<li>Locate the <strong>"VBoxHeadless.exe"</strong>  process and click <strong>"End process"</strong></li>
													</ul>
												</li>
												<li>
													<strong>Linux</strong>
													<ul style="margin-left:15px">
														<li>Open a terminal</li>
														<li>
															Assuming that you are not running any other Virtual Machine in your computer, type: 
															<pre>killall VBoxHeadless</pre>
															and hit enter.  
														</li>
													</ul>
												</li>
												<li>
													<strong>MacOSX</strong>
													<ul style="margin-left:15px">
														<li>Close the challenge and WebAPI websites</li>
														<li>Open the <strong>"Activity Monitor"</strong> application from the Utilities</li>
														<li>Locate the <strong>"VBoxHeadless"</strong>  process and double-click to open the details. If it does'nt quit, click "Force Quit".</li>
														<li>Now open the challenge website and click Start Computing. The VM should start running again.</li>
													</ul>
												</li>
											</ul>
											<strong>IF IT STILL DOESN’T WORK</strong><br>In all the above cases, If you are still encountering errors, destroy your Virtual Machine using the trash icon at the bottom of the configuration panel and then refresh the interface. To open this panel, click on the gear symbol at the bottom of the Challenge Dashboard.                      If the VM still doesn’t work after removing it and restarting, then remove it again, restart your computer, and try again. In certain rare cases, you may need to restart your computer a second time. If none of the above works for you, contact us through the forum or via <a href="mailto:citizencyberlab@unige.ch">citizencyberlab@unige.ch</a> with details of what is going wrong.
